// Mock Data pre Repasovanú Techniku (with translation keys instead of raw strings if possible, but kept raw for products as they usually come from DB)
let rawProducts = [];
const SPREADSHEET_CSV_URL = 'https://docs.google.com/spreadsheets/d/1y4SaY0ZPwx5OrINtI27iDKPCJ4uQTpOwGzRlj5ulQQg/export?format=csv';

async function fetchProductsFromGoogleSheets() {
    try {
        const response = await fetch(SPREADSHEET_CSV_URL);
        const text = await response.text();
        const lines = text.split('\n');
        
        let idCount = 1;
        const products = [];
        const seen = new Set();
        
        lines.forEach(line => {
            let tokens = [];
            let insideQuote = false;
            let currentToken = '';
            for (let char of line) {
                if (char === '"') insideQuote = !insideQuote;
                else if (char === ',' && !insideQuote) {
                    tokens.push(currentToken);
                    currentToken = '';
                } else {
                    currentToken += char;
                }
            }
            tokens.push(currentToken);

            if (tokens.length < 2) return;
            
            let category = tokens[0].trim();
            let name = tokens[1].trim();
            if (!name || !category) return;
            
            if (category === 'Stocklist' || category === '') return;
            if (category.includes('PlayStation')) return; 

            let cleanName = name.replace(/^"|"$/g, '').trim();
            if (seen.has(cleanName)) return;
            seen.add(cleanName);
            
            let configRaw = tokens[2] ? tokens[2].trim().replace(/^"|"$/g, '') : '';
            let stockRaw = tokens[3] ? tokens[3].trim() : '';
            let stock = parseInt(stockRaw) || 1;
            
            let type = 'laptop';
            if (category.includes('AiO') || category.includes('PC Mini') || category.includes('PC SFF') || category.includes('Thin Client')) type = 'desktop';
            if (category.includes('PC gaming')) type = 'desktop';
            if (category.includes('LCD')) type = 'monitor';
            if (category === 'DOCK') type = 'accessory';
            
            let title = cleanName;
            let specs = category;
            
            title = title.replace(/,$/, '').replace(/"/g, '').trim();
            if (title.length > 50) title = title.substring(0, 50) + '...';
            
            let image = 'https://images.unsplash.com/photo-1593642632823-8f785ba67e45?auto=format&fit=crop&q=80&w=800'; 
            
            const titleLower = title.toLowerCase();
            let query = title;
            if (type === 'desktop') {
                query += ' desktop pc';
                if (titleLower.includes('aio') || titleLower.includes('all-in-one')) query += ' all in one';
            } else if (type === 'monitor') {
                query += ' monitor display front';
            } else if (type === 'accessory') {
                query += ' dock station port replicator front view high quality';
            } else {
                query += ' laptop computer front view';
            }
            
            // Dynamic image assignment using the Bing proxy hack as built earlier
            image = `https://tse2.mm.bing.net/th?q=${encodeURIComponent(query)}&w=600`;
            
            let finalDesc = configRaw || 'Presné špecifikácie na vyžiadanie.';
            let descSK = finalDesc, descCZ = finalDesc, descEN = finalDesc;

            products.push({
                id: idCount++,
                type,
                specs,
                image,
                title,
                stock,
                price: 'Na vyžiadanie', // Can map other columns if available
                desc: { sk: descSK, cz: descCZ, en: descEN }
            });
        });
        
        rawProducts = products;
    } catch (e) {
        console.error('Error fetching Google Sheets CSV:', e);
        rawProducts = []; // Fallback to empty on failure
    }
}

// Po načítaní DOM štruktúry
document.addEventListener('DOMContentLoaded', () => {
    
    // --- Translations Logic ---
    let lastOpenedProduct = null;
    let currentLang = localStorage.getItem('repasit-lang') || 'sk';
    const langSelect = document.getElementById('langSelect');
    if (langSelect) {
        langSelect.value = currentLang;
        langSelect.addEventListener('change', (e) => {
            setLanguage(e.target.value);
        });
    }

    function setLanguage(lang) {
        currentLang = lang;
        localStorage.setItem('repasit-lang', lang);
        document.documentElement.lang = lang;
        
        applyTranslations();
        renderProducts(document.querySelector('.filter-btn.active').getAttribute('data-filter'));
    }

    function applyTranslations() {
        const dict = translations[currentLang];
        if (!dict) return;

        // Translate text nodes
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (dict[key]) {
                el.innerHTML = dict[key];
            }
        });

        // Translate placeholders
        document.querySelectorAll('[data-i18n-ph]').forEach(el => {
            const key = el.getAttribute('data-i18n-ph');
            if (dict[key]) {
                el.placeholder = dict[key];
            }
        });
        
        // Translate title
        if(dict['head_title']) {
            document.title = dict['head_title'];
        }
        
        // Update modal title if it was opened
        if (lastOpenedProduct) {
             const tpl = dict['modal_tpl'] || "Produkt: <strong>{0}</strong>";
             document.getElementById('modalProductTarget').innerHTML = tpl.replace('{0}', lastOpenedProduct);
        } else {
             const modalTarget = document.getElementById('modalProductTarget');
             if(modalTarget && modalTarget.innerHTML.indexOf('trong') === -1) {
                 modalTarget.innerHTML = dict['modal_general'] || "General Inquiry";
             }
        }
    }


    // --- Theme Toggle Logic ---
    const themeToggleBtn = document.getElementById('themeToggle');
    const iconMoon = document.querySelector('.icon-moon');
    const iconSun = document.querySelector('.icon-sun');
    
    const savedTheme = localStorage.getItem('repasit-theme');
    const prefersLight = window.matchMedia('(prefers-color-scheme: light)').matches;
    
    if (savedTheme === 'light' || (!savedTheme && prefersLight)) {
        document.documentElement.setAttribute('data-theme', 'light');
        if(iconMoon && iconSun) {
            iconMoon.classList.remove('hidden');
            iconSun.classList.add('hidden');
        }
    } else {
        // default is dark
        if(iconMoon && iconSun) {
            iconMoon.classList.add('hidden');
            iconSun.classList.remove('hidden');
        }
    }
    
    if(themeToggleBtn) {
        themeToggleBtn.addEventListener('click', () => {
            const currentTheme = document.documentElement.getAttribute('data-theme');
            if (currentTheme === 'light') {
                document.documentElement.removeAttribute('data-theme');
                localStorage.setItem('repasit-theme', 'dark');
                if(iconMoon && iconSun) {
                    iconMoon.classList.add('hidden');
                    iconSun.classList.remove('hidden');
                }
            } else {
                document.documentElement.setAttribute('data-theme', 'light');
                localStorage.setItem('repasit-theme', 'light');
                if(iconMoon && iconSun) {
                    iconMoon.classList.remove('hidden');
                    iconSun.classList.add('hidden');
                }
            }
        });
    }

    // --- Navbar Scroll Effect ---
    const navbar = document.getElementById('navbar');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });

    // --- Render Products ---
    const productsGrid = document.getElementById('productsGrid');
    
    function renderProducts(filterType = 'all') {
        productsGrid.innerHTML = '';
        
        const filteredProducts = filterType === 'all' 
            ? rawProducts 
            : rawProducts.filter(p => p.type === filterType);
            
        const dict = translations[currentLang];
        
        filteredProducts.forEach(product => {
            const card = document.createElement('div');
            card.className = 'product-card fade-in';
            const desc = product.desc[currentLang] || product.desc.sk;
            const stockText = dict['product_stock'].replace('{count}', product.stock);
            
            card.innerHTML = `
                <div class="product-image-wrap">
                    <span class="product-badge">${dict['product_grade']}</span>
                    <img src="${product.image}" alt="${product.title}">
                </div>
                <div class="product-content">
                    <div class="product-header-info">
                        <div class="product-specs">${product.specs}</div>
                        <div class="product-stock">${stockText}</div>
                    </div>
                    <h3 class="product-title">${product.title}</h3>
                    <p class="product-desc">${desc}</p>
                    <div class="product-footer">
                        <span class="product-price">${product.price}</span>
                        <button class="btn btn-primary btn-contact" data-product="${product.title}">${dict['product_btn']}</button>
                    </div>
                </div>
            `;
            productsGrid.appendChild(card);
        });

        document.querySelectorAll('.btn-contact').forEach(btn => {
            btn.addEventListener('click', (e) => {
                const productName = e.target.getAttribute('data-product');
                openContactModal(productName);
            });
        });
    }

    // Initialize translations and fetch live products
    productsGrid.innerHTML = '<div style="text-align:center; padding: 40px; grid-column: 1/-1; color: var(--text-secondary);">Načítavam produkty z Google tabuľky...</div>';
    
    fetchProductsFromGoogleSheets().then(() => {
        applyTranslations();
        renderProducts(document.querySelector('.filter-btn.active').getAttribute('data-filter'));
    });

    // --- Filter functionality ---
    const filterBtns = document.querySelectorAll('.filter-btn');
    filterBtns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            filterBtns.forEach(b => b.classList.remove('active'));
            e.target.classList.add('active');
            
            const filterValue = e.target.getAttribute('data-filter');
            renderProducts(filterValue);
        });
    });

    // --- Modal functionality ---
    const modalOverlay = document.getElementById('contactModal');
    const closeModalBtn = document.getElementById('closeModal');
    const modalProductTarget = document.getElementById('modalProductTarget');
    const inquiryForm = document.getElementById('inquiryForm');
    const formSuccess = document.getElementById('formSuccess');

    function openContactModal(productName) {
        lastOpenedProduct = productName;
        const dict = translations[currentLang];
        
        if(productName) {
            const tpl = dict['modal_tpl'] || "Produkt: <strong>{0}</strong>";
            modalProductTarget.innerHTML = tpl.replace('{0}', productName);
        } else {
            modalProductTarget.innerHTML = dict['modal_general'];
        }
        
        inquiryForm.reset();
        inquiryForm.style.display = 'block';
        formSuccess.classList.add('hidden');
        
        modalOverlay.classList.add('active');
        document.body.style.overflow = 'hidden';
    }

    function closeContactModal() {
        modalOverlay.classList.remove('active');
        document.body.style.overflow = '';
        lastOpenedProduct = null;
    }

    closeModalBtn.addEventListener('click', closeContactModal);
    modalOverlay.addEventListener('click', (e) => {
        if (e.target === modalOverlay) closeContactModal();
    });

    inquiryForm.addEventListener('submit', async (e) => {
        e.preventDefault();
        
        const submitBtn = inquiryForm.querySelector('.btn-primary');
        const originalText = submitBtn.innerText;
        submitBtn.innerText = 'Odosielam...';
        submitBtn.disabled = true;
        
        const formData = new FormData(inquiryForm);
        formData.append("access_key", "60dbcb5e-5044-49d4-9afa-a414c5df421a");
        
        const productName = document.getElementById('modalProductName').innerText;
        const formSubject = productName === '' ? 'Všeobecný dopyt' : `Záujem o produkt: ${productName}`;
        formData.append("subject", formSubject);
        formData.append("from_name", "REPASit E-shop");

        try {
            const response = await fetch("https://api.web3forms.com/submit", {
                method: "POST",
                body: formData
            });
            
            const data = await response.json();
            
            if (data.success) {
                inquiryForm.style.display = 'none';
                formSuccess.classList.remove('hidden');
                setTimeout(() => closeContactModal(), 3000);
            } else {
                alert("Nastala chyba pri odosielaní. Skúste to prosím neskôr.");
            }
        } catch (error) {
            alert("Skontrolujte svoje internetové pripojenie.");
        } finally {
            submitBtn.innerText = originalText;
            submitBtn.disabled = false;
        }
    });
    
    // Navbar Generic Contact Button
    document.querySelector('.nav-btn').addEventListener('click', (e) => {
        e.preventDefault();
        openContactModal(null);
    });

});
